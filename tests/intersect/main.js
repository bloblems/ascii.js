"use strict";

////////////////////////////////////////////////////////////////////////////////
/// DATA
////////////////////////////////////////////////////////////////////////////////

const		BOXES	= 20;
const		MIN		= 5;
const		MAX		= 30;

////////////////////////////////////////////////////////////////////////////////
/// FUNCTIONS
////////////////////////////////////////////////////////////////////////////////

function	setup() {
	let		i;
	let		x, y;
	let		width, height;

	create_canvas();
	set_box_mode(BOX_CENTER);
	for (i = 0; i < BOXES; ++i) {
		x = floor(random(0, canvas_width));
		y = floor(random(0, canvas_height));
		width = floor(random(MIN, MAX));
		height = floor(random(MIN, MAX));
		box(x, y, width, height);
	}
	//box(11, 5, 10, 6);
	//box(10, 10, 10, 6);
}

function	draw() {
	no_loop();
}
